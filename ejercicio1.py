# Definir los números
num1 = 5
num2 = 4

# Realizar las operaciones
suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
division_entera = num1 // num2
modulo = num1 % num2
potencia = num1 ** num2

# Mostrar los resultados de una forma clara
print(f"Resultados para los números {num1} y {num2}:")
print(f"Suma: {num1} + {num2} = {suma}")
print(f"Resta: {num1} - {num2} = {resta}")
print(f"Multiplicación: {num1} * {num2} = {multiplicacion}")
print(f"División: {num1} / {num2} = {division}")
print(f"División entera: {num1} // {num2} = {division_entera}")
print(f"Módulo: {num1} % {num2} = {modulo}")
print(f"Potencia: {num1} ** {num2} = {potencia}")
